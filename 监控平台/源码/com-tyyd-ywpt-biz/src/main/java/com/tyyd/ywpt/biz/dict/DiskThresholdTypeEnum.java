package com.tyyd.ywpt.biz.dict;

/**
 * @author wangyu
 *
 */
public enum DiskThresholdTypeEnum {

	No_Threshold(0,"不需要预警"),
	Percent_Threshold(1,"百分比预警"),
	Free_Threshold(2,"剩余容量预警");
	
	private int type;
	private String remark;
	/**
	 * @return the type
	 */
	public int getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(int type) {
		this.type = type;
	}
	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}
	/**
	 * @param remark the remark to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}
	/**
	 * @param type
	 * @param remark
	 */
	private DiskThresholdTypeEnum(int type, String remark) {
		this.type = type;
		this.remark = remark;
	}
	
	
}
