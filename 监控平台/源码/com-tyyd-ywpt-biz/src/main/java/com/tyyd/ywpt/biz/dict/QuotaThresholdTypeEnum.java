package com.tyyd.ywpt.biz.dict;

/**
 * @author wangyu
 *
 */
public enum QuotaThresholdTypeEnum {

	compare_big_warn(1,"直接比大小，大的预警"),
	d_value_warn(2,"上一次的差值预警"),
	compare_small_warn(3,"直接比大小，小的预警");
	
	private int type;
	private String remark;
	/**
	 * @return the type
	 */
	public int getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(int type) {
		this.type = type;
	}
	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}
	/**
	 * @param remark the remark to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}
	/**
	 * @param type
	 * @param remark
	 */
	private QuotaThresholdTypeEnum(int type, String remark) {
		this.type = type;
		this.remark = remark;
	}
	
	
}
