package com.tyyd.ywpt.dao.alert.record.dataobject;

import java.util.Date;

/**
 * @author wangyu
 *
 */
public class AlertNotifyRecordDomain {

	private String id;

    /**
     * 用户ID
     */
    private String userId;
    
    /**
     * 告警记录ID
     */
    private String alertRecordId;

    /**
     * 收件人,分号分割
     */
    private String receviceMails;
    
    /**
     * 手机收件人，分号分割
     */
    private String mobileMails;

    /**
     * 是否处理,0:否，1：是
     */
    private String isComplete;

    /**
     * 创建时间
     */
    private Date gmtCreated;

    /**
     * 修改时间
     */
    private Date gmtModifed;
    
    /**
     * 通知类型
     * 
     */
    private Integer noticeType;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the userId
	 */
	public String getUserId() {
		return userId;
	}

	/**
	 * @param userId the userId to set
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}

	/**
	 * @return the receviceMails
	 */
	public String getReceviceMails() {
		return receviceMails;
	}

	/**
	 * @param receviceMails the receviceMails to set
	 */
	public void setReceviceMails(String receviceMails) {
		this.receviceMails = receviceMails;
	}

	/**
	 * @return the mobileMails
	 */
	public String getMobileMails() {
		return mobileMails;
	}

	/**
	 * @param mobileMails the mobileMails to set
	 */
	public void setMobileMails(String mobileMails) {
		this.mobileMails = mobileMails;
	}

	/**
	 * @return the isComplete
	 */
	public String getIsComplete() {
		return isComplete;
	}

	/**
	 * @param isComplete the isComplete to set
	 */
	public void setIsComplete(String isComplete) {
		this.isComplete = isComplete;
	}

	/**
	 * @return the gmtCreated
	 */
	public Date getGmtCreated() {
		return gmtCreated;
	}

	/**
	 * @param gmtCreated the gmtCreated to set
	 */
	public void setGmtCreated(Date gmtCreated) {
		this.gmtCreated = gmtCreated;
	}

	/**
	 * @return the gmtModifed
	 */
	public Date getGmtModifed() {
		return gmtModifed;
	}

	/**
	 * @param gmtModifed the gmtModifed to set
	 */
	public void setGmtModifed(Date gmtModifed) {
		this.gmtModifed = gmtModifed;
	}

	/**
	 * @return the alertRecordId
	 */
	public String getAlertRecordId() {
		return alertRecordId;
	}

	/**
	 * @param alertRecordId the alertRecordId to set
	 */
	public void setAlertRecordId(String alertRecordId) {
		this.alertRecordId = alertRecordId;
	}

	/**
	 * @return the noticeType
	 */
	public Integer getNoticeType() {
		return noticeType;
	}

	/**
	 * @param noticeType the noticeType to set
	 */
	public void setNoticeType(Integer noticeType) {
		this.noticeType = noticeType;
	}

}
