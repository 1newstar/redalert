package com.tyyd.ywpt.dao.configration.relation.dataobject;

import java.util.Date;

import org.apache.commons.lang.builder.ToStringBuilder;

/**
 * @author wangyu
 *
 */
public class DbRelationDomain {

	/**
	 * 主键
	 */
	private String id;

    /**
     * 主机
     */
    private String hostId;
    
    /**
     * 主机的名称
     */
    private String hostName;
    
    /**
     * DB的名称
     */
    private String nickName;

    /**
     * 数据库ID
     */
    private String dbId;

    /**
     * 关系大类型,1:主从，2：集群 ,见字典db_relation_type
     */
    private Integer relationType;

    /**
     * 是否只读,0:是,1:否
     */
    private String isReadonly;

    /**
     * 是否主机,0:是,1:否
     */
    private String isMaster;

    /**
     * 归类ID
     */
    private String batchId;

   

    /**
     * 创建时间
     */
    private Date gmtCreated;

    /**
     * 修改时间
     */
    private Date gmtModifed;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the hostId
	 */
	public String getHostId() {
		return hostId;
	}

	/**
	 * @param hostId the hostId to set
	 */
	public void setHostId(String hostId) {
		this.hostId = hostId;
	}

	/**
	 * @return the dbId
	 */
	public String getDbId() {
		return dbId;
	}

	/**
	 * @param dbId the dbId to set
	 */
	public void setDbId(String dbId) {
		this.dbId = dbId;
	}

	/**
	 * @return the relationType
	 */
	public Integer getRelationType() {
		return relationType;
	}

	/**
	 * @param relationType the relationType to set
	 */
	public void setRelationType(Integer relationType) {
		this.relationType = relationType;
	}

	/**
	 * @return the isReadonly
	 */
	public String getIsReadonly() {
		return isReadonly;
	}

	/**
	 * @param isReadonly the isReadonly to set
	 */
	public void setIsReadonly(String isReadonly) {
		this.isReadonly = isReadonly;
	}

	/**
	 * @return the isMaster
	 */
	public String getIsMaster() {
		return isMaster;
	}

	/**
	 * @param isMaster the isMaster to set
	 */
	public void setIsMaster(String isMaster) {
		this.isMaster = isMaster;
	}

	/**
	 * @return the batchId
	 */
	public String getBatchId() {
		return batchId;
	}

	/**
	 * @param batchId the batchId to set
	 */
	public void setBatchId(String batchId) {
		this.batchId = batchId;
	}



	/**
	 * @return the gmtCreated
	 */
	public Date getGmtCreated() {
		return gmtCreated;
	}

	/**
	 * @param gmtCreated the gmtCreated to set
	 */
	public void setGmtCreated(Date gmtCreated) {
		this.gmtCreated = gmtCreated;
	}

	/**
	 * @return the gmtModifed
	 */
	public Date getGmtModifed() {
		return gmtModifed;
	}

	/**
	 * @param gmtModifed the gmtModifed to set
	 */
	public void setGmtModifed(Date gmtModifed) {
		this.gmtModifed = gmtModifed;
	}

	/**
	 * @return the hostName
	 */
	public String getHostName() {
		return hostName;
	}

	/**
	 * @param hostName the hostName to set
	 */
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}

	/**
	 * @return the nickName
	 */
	public String getNickName() {
		return nickName;
	}

	/**
	 * @param nickName the nickName to set
	 */
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this);
	}
    
	
    
}
