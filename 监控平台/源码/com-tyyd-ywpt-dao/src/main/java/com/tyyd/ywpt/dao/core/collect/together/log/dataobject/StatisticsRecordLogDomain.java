package com.tyyd.ywpt.dao.core.collect.together.log.dataobject;

import java.util.Date;

/**
 * @author wangyu
 * 
 */
public class StatisticsRecordLogDomain {

	private Integer id;

	/**
	 * 系统类型
	 */
	private Integer sysType;

	/**
	 * 统计类型,0: 历史迁移,1: 4小时任务,2:日任务,3:周任务
	 */
	private Integer statType;

	/**
	 * 处理时间
	 */
	private Date workTime;
	
	private String workTimeStr;

	private Date gmtCreated;

	private Date gmtModifed;

	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * @return the sysType
	 */
	public Integer getSysType() {
		return sysType;
	}

	/**
	 * @param sysType the sysType to set
	 */
	public void setSysType(Integer sysType) {
		this.sysType = sysType;
	}

	/**
	 * @return the statType
	 */
	public Integer getStatType() {
		return statType;
	}

	/**
	 * @param statType the statType to set
	 */
	public void setStatType(Integer statType) {
		this.statType = statType;
	}

	/**
	 * @return the workTime
	 */
	public Date getWorkTime() {
		return workTime;
	}

	/**
	 * @param workTime the workTime to set
	 */
	public void setWorkTime(Date workTime) {
		this.workTime = workTime;
	}

	/**
	 * @return the gmtCreated
	 */
	public Date getGmtCreated() {
		return gmtCreated;
	}

	/**
	 * @param gmtCreated the gmtCreated to set
	 */
	public void setGmtCreated(Date gmtCreated) {
		this.gmtCreated = gmtCreated;
	}

	/**
	 * @return the gmtModifed
	 */
	public Date getGmtModifed() {
		return gmtModifed;
	}

	/**
	 * @param gmtModifed the gmtModifed to set
	 */
	public void setGmtModifed(Date gmtModifed) {
		this.gmtModifed = gmtModifed;
	}

	/**
	 * @return the workTimeStr
	 */
	public String getWorkTimeStr() {
		return workTimeStr;
	}

	/**
	 * @param workTimeStr the workTimeStr to set
	 */
	public void setWorkTimeStr(String workTimeStr) {
		this.workTimeStr = workTimeStr;
	}
	
	
	
}
