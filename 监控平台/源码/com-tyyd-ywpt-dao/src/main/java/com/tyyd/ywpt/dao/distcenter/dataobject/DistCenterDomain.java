package com.tyyd.ywpt.dao.distcenter.dataobject;

/**
 * @author wangyu
 *
 */
public class DistCenterDomain {

	
	private Long id;
	
	private String keyName;
	
	private String keyValue;
	
	private String keyComments;
	
	private String keyType;
	
	private String keyColumn;
	
	
	/**
	 * @return the keyColumn
	 */
	public String getKeyColumn() {
		return keyColumn;
	}

	/**
	 * @param keyColumn the keyColumn to set
	 */
	public void setKeyColumn(String keyColumn) {
		this.keyColumn = keyColumn;
	}

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the keyName
	 */
	public String getKeyName() {
		return keyName;
	}

	/**
	 * @param keyName the keyName to set
	 */
	public void setKeyName(String keyName) {
		this.keyName = keyName;
	}

	/**
	 * @return the keyValue
	 */
	public String getKeyValue() {
		return keyValue;
	}

	/**
	 * @param keyValue the keyValue to set
	 */
	public void setKeyValue(String keyValue) {
		this.keyValue = keyValue;
	}

	/**
	 * @return the keyComments
	 */
	public String getKeyComments() {
		return keyComments;
	}

	/**
	 * @param keyComments the keyComments to set
	 */
	public void setKeyComments(String keyComments) {
		this.keyComments = keyComments;
	}

	/**
	 * @return the keyType
	 */
	public String getKeyType() {
		return keyType;
	}

	/**
	 * @param keyType the keyType to set
	 */
	public void setKeyType(String keyType) {
		this.keyType = keyType;
	}
	
	
	
}
