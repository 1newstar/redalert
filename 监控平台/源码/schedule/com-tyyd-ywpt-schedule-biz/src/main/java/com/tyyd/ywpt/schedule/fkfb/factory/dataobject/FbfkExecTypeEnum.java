package com.tyyd.ywpt.schedule.fkfb.factory.dataobject;

/**
 * @author wangyu
 *
 */
public enum FbfkExecTypeEnum {

	shellCmd(1,"脚本"),
	sqlCmd(2,"sql");
	
	private Integer id;
	private String remark;
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}
	/**
	 * @param remark the remark to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}
	/**
	 * @param id
	 * @param remark
	 */
	private FbfkExecTypeEnum(Integer id, String remark) {
		this.id = id;
		this.remark = remark;
	}
	
	
	
}
